package operator;

public class ArithmaticOperator {
//% reminder 10/3= 1 is the reminder

    public static void main(String[] args) {
        int num1=40;
        int num2= 50;

        int summation= num1+num2;
        System.out.println(" total is subm"+summation);
        int subtraction= num1-num2;
        System.out.println(" total is substr"+subtraction);
        int multiply= num1*num2;
        System.out.println(" total is multi "+multiply);
        int division= num1/num2;
        System.out.println(" total is division "+division);
        int remainder= num1%num2;
        System.out.println(" total is remainder "+remainder);
    }
}
